// Example
let students = ["Abhigyan", "Derek", "James", "Patrick"];

// Nested Array
let school = [
    ["Narayana", 0],
    ["Sanskriti The Gurukul", 1],
];

// Accessing data in an array
student1 = students[0];
student2 = students[1];

// Accessing Multi-dimensional arrays
school1 = school[0][0];
school2 = school[1][0];

// Adding data to an array
students.push("John"); // Adds an element to the end of an array
students.unshift("Jane"); // Adds an element to the beginning of an array

// Removing data from an array
students.pop(); // Removes from the end of an array
students.shift(); // Removes from the beginning of an array
