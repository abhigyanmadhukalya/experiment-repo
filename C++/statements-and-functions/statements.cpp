#include <iostream>

int main(int argc, char **argv)
{

    int firstNumber = 12;  // This is a statement in C++
    int secondNumber = 14; // This is also a statement

    /*
        Every line in the main() function of a C++ program source code is a statement
    */

    int sum = firstNumber + secondNumber;

    std::cout << "The sum of the numbers is: " << sum << std::endl;

    return 0;
}