# Tuples --> Ordered, Immutable, allows for duplicate elements

myTuple = ("Abhigyan", 18, "India")
print(myTuple)

# The comma is required for it to be recognised as a tuple
tuple2 = ("Abhigyan",)
print(type(tuple2))

# Enumerations, I guess?
